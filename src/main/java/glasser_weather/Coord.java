package glasser_weather;

public class Coord {
    private double lon;
    private double lat;

    public double getLon() {
        return lon;
    }

    public void setLon(int input) {
        this.lon = input;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(int input) {
        this.lat = input;
    }
}
